package org.netty.other.constants;

/**
 * @author luowx on 2019/3/21 0021.
 */
public class DateFormatConstants {

    /**
     * 横线分隔日期，示例2020-01-01
     */
    public static final String DATE_FOR_LINE = "yyyy-MM-dd";

    /**
     * 斜杆分隔日期，示例2020/01/01
     */
    public static final String DATE_FOR_DIAGONAL = "yyyy/MM/dd";

    /**
     * 无分隔日期，示例20200101
     */
    public static final String DATE_FOR_NONE = "yyyyMMdd";

    /**
     * 无分隔日期时间，示例20200101235959
     */
    public static final String DATE_TIME_FOR_NONE = "yyyyMMddHHmmss";

    /**
     * 横线分隔日期时间，示例2020-01-01 23:59:59
     */
    public static final String DATE_TIME_FOR_LINE = "yyyy-MM-dd HH:mm:ss";

    /**
     * 斜杠分隔日期时间，示例2020/01/01/23/59/59
     */
    public static final String DATE_TIME_FOR_DIAGONAL = "yyyy/MM/dd/HH/mm/ss";

    /**
     * 冒号分隔时间，示例23:59:59
     */
    public static final String TIME_FOR_COLON = "HH:mm:ss";
}
